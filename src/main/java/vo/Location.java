package vo;

public class Location {
	
	private int no;
	private String name;
	
	public Location() {}
	
	public Location(int no) {
		this.no = no;
	}
	
	public int getNo() {
		return no;
	}
	
	public void setNo(int no) {
		this.no = no;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
}
